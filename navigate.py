def visualize_graph_with_background(graph, image_path, path=None, path_color='red', path_width=2):
    """Визуализирует граф с путём на фоне изображения. ИСХОДНЫЙ КОД НЕТРОНУТЫЙ."""
    try:
        img = Image.open(image_path)
    except FileNotFoundError:
        return None, f"Изображение не найдено: {image_path}"
    except Exception as e:
        return None, f"Ошибка загрузки изображения: {e}"

    pos = nx.get_node_attributes(graph, 'pos')
    if not pos:
        return None, "Ошибка: координаты узлов не найдены"

    fig, ax = plt.subplots(figsize=(22, 16))
    ax.imshow(img)

    if path:
        path_edges = list(zip(path, path[1:]))
        try:
            path_edges.remove(("л21", "л31"))
        except ValueError:
            pass
        nx.draw_networkx_edges(graph, pos, ax=ax, edgelist=path_edges, edge_color=path_color, width=path_width)
    else:
        return None, "Путь пуст"

    plt.axis('off')
    
    # Преобразование в base64 SVG
    buffer = io.BytesIO()
    plt.savefig(buffer, format='svg', bbox_inches='tight', dpi=100, pad_inches=0.1)
    plt.close()
    buffer.seek(0)
    image_data = base64.b64encode(buffer.read()).decode()
    return f"data:image/svg+xml;base64,{image_data}", None


def is_valid_room(room_name):
    """Проверяет, является ли строка валидным кабинетом (не коридором)."""
    if room_name.startswith('/') or room_name.startswith('//'):
        return False
    
    allowed_special = ['тж', 'тм', 'л21', 'л22', 'л23', 'л24', 'л25', 'л31',
                       'Учебный отдел', 'Зал заседаний', '301', '302', '303', '307а']
    if room_name in allowed_special:
        return True
    
    if room_name and (room_name[0].isdigit() or room_name[0].isalpha()):
        return True
    
    return False

def get_floor_by_room(room_name):
    """Определяет этаж по названию кабинета"""
    if room_name in ['Зал заседаний', 'Учебный отдел']:
        return 3
    if room_name.startswith('3') and room_name!='3':
        return 3
    if room_name in ['301', '302', '303', '307а']:
        return 3
    
    return 2

def get_map_path(floor):
    """Возвращает путь к файлу карты этажа"""
    primary_path = f"./static/maps/map_floor{floor}.jpg"
    if os.path.exists(primary_path):
        return primary_path
    alternative_paths = [
        f"static/maps/map_floor{floor}.jpg",
        f"./maps/map_floor{floor}.jpg",
        f"maps/map_floor{floor}.jpg",
        f"./static/maps/floor{floor}.jpg",
        f"map_floor{floor}.jpg",
    ]
    
    for path in alternative_paths:
        if os.path.exists(path):
            return path
    return primary_path

def generate_route(room_from, room_to):
    """Основная функция интеграции алгоритма в API. Возвращает JSON с результатом маршрутизации"""
    if len(G.nodes()) == 0:
        add_to_graf()
    if not room_from or not room_to:
        return {
            "success": False,
            "error": "Оба поля должны быть заполнены",
            "path": None,
            "image": None,
            "floor": None
        }
    if not is_valid_room(room_from):
        return {
            "success": False,
            "error": f"'{room_from}' - это коридор, нужно выбрать кабинет",
            "path": None,
            "image": None,
            "floor": None
        }
    if not is_valid_room(room_to):
        return {
            "success": False,
            "error": f"'{room_to}' - это коридор, нужно выбрать кабинет",
            "path": None,
            "image": None,
            "floor": None
        }
    if room_from not in G:
        return {
            "success": False,
            "error": f"Кабинет '{room_from}' не найден",
            "path": None,
            "image": None,
            "floor": None
        }
    if room_to not in G:
        return {
            "success": False,
            "error": f"Кабинет '{room_to}' не найден",
            "path": None,
            "image": None,
            "floor": None
        }
    if room_from == room_to:
        return {
            "success": False,
            "error": "Начало и конец маршрута не могут быть одинаковыми",
            "path": None,
            "image": None,
            "floor": None
        }
    try:
        if (get_floor_by_room(room_to) == 3 and get_floor_by_room(room_from) == 2):
            path = nx.shortest_path(G, room_from, room_to)
        elif (get_floor_by_room(room_to) == 2 and get_floor_by_room(room_from) == 3):
            path = nx.shortest_path(G, room_to, room_from)
        else:
            path = nx.shortest_path(G, room_from, room_to)
            
        floor = get_floor_by_room(room_to)
        
        if (get_floor_by_room(room_to) == 3 or get_floor_by_room(room_from) == 3) and (get_floor_by_room(room_to) != get_floor_by_room(room_from)):
            map_path = get_map_path(3)
        else:
            map_path = get_map_path(floor)
            
        image_data, error = visualize_graph_with_background(G, map_path, path=path)
        
        if error:
            return {
                "success": False,
                "error": error,
                "path": path,
                "image": None,
                "floor": floor
            }
        
        return {
            "success": True,
            "path": path,
            "image": image_data,
            "floor": floor,
            "floor_svg": image_data
        }
        
    except nx.NetworkXNoPath:
        return {
            "success": False,
            "error": "Маршрут между указанными кабинетами не существует",
            "path": None,
            "image": None,
            "floor": get_floor_by_room(room_to)
        }
    except Exception as e:
        return {
            "success": False,
            "error": f"Ошибка при построении маршрута: {str(e)}",
            "path": None,
            "image": None,
            "floor": None
        }
